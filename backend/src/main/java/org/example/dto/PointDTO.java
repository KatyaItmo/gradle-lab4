package org.example.dto;

public class PointDTO {
	private String x;
	private String y;
	private String r;
	private boolean click;
	
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	public String getY() {
		return y;
	}
	public void setY(String y) {
		this.y = y;
	}
	public String getR() {
		return r;
	}
	public void setR(String r) {
		this.r = r;
	}
	public boolean getClick() {
		return click;
	}
	public void setClick(boolean click) {
		this.click = click;
	}
}
